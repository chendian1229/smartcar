#include "common.h"
#include "MK60_port.h"
#include "MK60_ftm.h"
#include "encoder.h"
/**********************************************************************************************
                                 初始化正交解码FTM1 FTM2
**********************************************************************************************/
void Encoder_Init()
{
  ftm_quad_init(FTM1);//左轮编码器 FTM1 A:PTA12 B:PTA13
  ftm_quad_init(FTM2);//右轮编码器 FTM2 A:PTA10 B:PTA11
}
/**********************************************************************************************
                                   获取左轮编码器计数
**********************************************************************************************/
int16 Get_Encoder_Left()
{
 return ftm_quad_get(FTM1);
}
/**********************************************************************************************
                                   获取右轮编码器计数
**********************************************************************************************/
int16 Get_Encoder_Right()
{
 return ftm_quad_get(FTM2);
}
/**********************************************************************************************
                                   左右编码器计数清零
**********************************************************************************************/
void Encoder_Clear()
{
  ftm_quad_clean(FTM1);
  ftm_quad_clean(FTM2);
}